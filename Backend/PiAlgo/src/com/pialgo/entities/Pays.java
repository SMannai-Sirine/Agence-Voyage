package model;

/**
 *
 * @author Dell
 */
public class Pays {
    private int id;
    private String nom;

    public Pays() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    @Override
    public String toString() {
        return "Pays{" + "id=" + id + ", nom=" + nom + '}';
    }
    
    
    
}
